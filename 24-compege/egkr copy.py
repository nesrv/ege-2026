'''
Задание 24 (№25361).

Текстовый файл 24-compege\24_23762.txt состоит из десятичных цифр и заглавных букв латинского алфавита. Определите в прилагаемом файле максимальное количество идущих подряд символов, среди которых буква F встречается ровно 76 раз, четная цифра встречается ровно один раз, искомая последовательность начинается с этой единственной четной цифры.

В ответе запишите число – количество символов в найденной последовательности.

Для выполнения этого задания следует написать программу.

'''

data = open('24-compege/24_23762.txt').read()

max_len = 0
n = len(data)

# Проходим по всем позициям как началам последовательности
for start in range(n):
    # Проверяем, начинается ли последовательность с четной цифры
    if data[start] not in "02468":
        continue
    
    count_F = 0
    count_even_digits = 0
    
    # Идем от start до конца
    for end in range(start, n):
        char = data[end]
        
        # Подсчет букв F
        if char == "F":
            count_F += 1
        
        # Подсчет четных цифр
        if char in "02468":
            count_even_digits += 1
        
        # Условия задачи:
        # 1) ровно 76 букв F
        # 2) ровно 1 четная цифра (она же первая)
        if count_F == 76 and count_even_digits == 1:
            length = end - start + 1
            if length > max_len:
                max_len = length
        
        # Если условия нарушены, прерываем
        if count_F > 76 or count_even_digits > 1:
            break

print(max_len)
