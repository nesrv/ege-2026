'''
Текстовый файл состоит из десятичных цифр и заглавных букв латинского алфавита. 
Определите в прилагаемом файле максимальное количество идущих подряд символов, 
среди которых подстрока 2025 встречается не менее 90раз и при этом содержится ровно 80букв Y.
'''

from re import findall

s = open('24-actual/DEMO_24.txt').read()

# Ищем все подстроки с ровно 80 буквами Y
pattern = r'(?=((?:[^Y]*Y){80}[^Y]*))'
matches = findall(pattern, s)

maxi = 0
for m in matches:
    if m.count('2025') >= 90:
        maxi = max(maxi, len(m))

print(maxi)

# Объяснение:
# (?=...) - lookahead для поиска всех перекрывающихся совпадений
# (?:[^Y]*Y){80} - участок до Y и сама Y (повторяем 80 раз)
# [^Y]* - участок после последней Y
# Проверяем условие: не менее 90 вхождений '2025'
