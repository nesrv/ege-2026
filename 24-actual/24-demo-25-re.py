from re import findall
s = open('24-actual/demo_2025_24.txt').read()
matches = findall(r'(?:0|[6-9][6-9]*)(?:[*-](?:0|[6-9][6-9]*))*', s)
maxi = max(matches, key=len)
print(len(maxi))

# Объяснение:
# (?:0|[6-9][6-9]*) - число: либо 0, либо начинается с 6-9 (без ведущих нулей)
# (?:[*-](?:0|[6-9][6-9]*))* - повторение: операция (*/-) и число
# Это гарантирует: нет рядом стоящих операций, нет ведущих нулей
# max(..., key=len) - находит самую длинную строку
