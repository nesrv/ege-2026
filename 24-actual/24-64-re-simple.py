from re import findall
s = open('24-actual/24 (17).txt').read()
pattern = r'(?=([1-9]+(?:[+*][1-9]+){49}))'
matches = findall(pattern, s)
maxi = max(matches, key=len)
print(len(maxi))

# Объяснение:
# (?=...) - lookahead, ищет все позиции, где начинается выражение
# [1-9]+ - первое число (одна или более цифр 1-9)
# (?:[+*][1-9]+){49} - повторить 49 раз: операция (+/*) и число
# Итого: 50 чисел с 49 операциями между ними
# max(..., key=len) - находит самую длинную строку
