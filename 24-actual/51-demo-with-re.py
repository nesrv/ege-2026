import re
s = open("24-actual/demo_2025_24.txt").read()
matches = re.findall(r'(?:0|[6-9]\d*)(?:[*-](?:0|[6-9]\d*))*', s)
print(max(len(m) for m in matches))

# Объяснение:
# (?:0|[6-9]\d*) - число: либо 0, либо начинается с 6-9 (без ведущих нулей)
# (?:[*-](?:0|[6-9]\d*))* - повторение: операция (* или -) и число
# Это гарантирует: нет рядом стоящих операций, нет ведущих нулей
