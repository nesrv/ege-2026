# Задача: найти максимальную длину подстроки, содержащей не более 80 букв Y
# и не менее 90 вхождений подстроки '2025'

s = open('24-actual/DEMO_24.txt').read()

# Разбиваем строку по букве 'Y'
# Получаем список участков между буквами Y
# Например: "ABC Y DEF Y GH" -> ["ABC ", " DEF ", " GH"]
parts = s.split('Y')

# Для каждого участка запоминаем:
# - длину участка (количество символов)
lengths = [len(p) for p in parts]
# - количество вхождений '2025' в этом участке
counts = [p.count('2025') for p in parts]

max_length = 0

# Берём 81 подряд идущий участок
# Между 81 участком будет ровно 80 букв 'Y'
# Перебираем все возможные начальные позиции
for i in range(len(parts) - 80):
    # Количество букв Y между 81 участком
    total_Y = 80
    # Общая длина = сумма длин 81 участка + 80 букв Y между ними
    total_len = sum(lengths[i:i+81]) + total_Y
    # Общее количество '2025' в этих 81 участках
    total_2025 = sum(counts[i:i+81])
    # Проверяем условие: не менее 90 вхождений '2025'
    if total_2025 >= 90:
        max_length = max(max_length, total_len)

print(max_length)

