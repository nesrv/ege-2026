t = open('24-actual/69932.txt').read()

# Заменяем все запрещённые комбинации на 'x'
for bad in ['**','--','*-','-*','*0','0*','-0','0-']:
    t = t.replace(bad,'x')

# Разбиваем на кандидаты
a = t.split('x')

# Считаем длину максимальной подпоследовательности, убирая '*' и '-' на концах
ml = max((len(i.strip('*-')) for i in a), default=0)

print(ml)