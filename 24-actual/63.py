'''
Текстовый файл состоит из десятичных цифр и заглавных букв латинского алфавита. 
Определите в прилагаемом файле максимальное количество идущих подряд символов, 
среди которых подстрока 2025 встречается не менее 90раз и при этом содержится ровно 80букв Y.

В ответе запишите число— количество символов в найденной последовательности.

Для выполнения этого задания следует написать программу.
'''

s = open('24-actual/DEMO_24.txt').read()

parts = s.split('Y')
lengths = [len(p) for p in parts]
counts = [p.count('2025') for p in parts]

maxi = 0
for i in range(len(parts) - 80):
    total_len = sum(lengths[i:i+81]) + 80
    total_2025 = sum(counts[i:i+81])
    if total_2025 >= 90:
        maxi = max(maxi, total_len)

print(maxi)

